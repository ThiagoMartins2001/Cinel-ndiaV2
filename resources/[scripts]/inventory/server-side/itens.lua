	["chip"] = function(source,Passport,Amount,Slot,Full,Item,Split)
        TriggerClientEvent("inventory:Close",source)

        local Keyboard = vKEYBOARD.Secondary(source,"Três primeiros digitos","Três ultimos digitos")
        if not Keyboard then return end

		local Primary = sanitizeString(Keyboard[1],"0123456789",true)
		local Secondary = sanitizeString(Keyboard[2],"0123456789",true)
		if not string.len(Primary) == 3 or not string.len(Secondary) == 3 then
			TriggerClientEvent("Notify",source,"vermelho","Necessário possuir 6 números.",5000)
			return
		end

		local newPhone = Primary..'-'..Secondary
		if vRP.UserPhone(newPhone) then
			TriggerClientEvent("Notify",source,"amarelo","Número já existente.",5000)
			return
		end

		if not vRP.TakeItem(Passport,Full,1,true,Slot) then return end
		local oldPhone = vRP.Identity(Passport).phone

		sql:execute("UPDATE characters SET phone = \'"..newPhone.."\' WHERE id = \'"..Passport.."\'")
		sql:execute("UPDATE smartphone_blocks SET phone = \'"..newPhone.."\' WHERE phone = \'"..oldPhone.."\'")
		sql:execute("UPDATE smartphone_calls SET initiator = \'"..newPhone.."\' WHERE initiator = \'"..oldPhone.."\'")
		sql:execute("UPDATE smartphone_calls SET target = \'"..newPhone.."\' WHERE target = \'"..oldPhone.."\'")
		sql:execute("UPDATE smartphone_contacts SET owner = \'"..newPhone.."\' WHERE owner = \'"..oldPhone.."\'")
		sql:execute("UPDATE smartphone_contacts SET phone = \'"..newPhone.."\' WHERE phone = \'"..oldPhone.."\'")
		sql:execute("UPDATE smartphone_whatsapp SET owner = \'"..newPhone.."\' WHERE owner = \'"..oldPhone.."\'")
		sql:execute("UPDATE smartphone_whatsapp_channels SET sender = \'"..newPhone.."\' WHERE sender = \'"..oldPhone.."\'")
		sql:execute("UPDATE smartphone_whatsapp_channels SET target = \'"..newPhone.."\' WHERE target = \'"..oldPhone.."\'")
		sql:execute("UPDATE smartphone_whatsapp_messages SET sender = \'"..newPhone.."\' WHERE sender = \'"..oldPhone.."\'")
		sql:execute("UPDATE smartphone_whatsapp_groups SET owner = \'"..newPhone.."\' WHERE owner = \'"..oldPhone.."\'")
	
		local groupsMembers = sql:query_async("SELECT * FROM smartphone_whatsapp_groups")
		for k,v in pairs(groupsMembers) do
			local members = json.decode(v.members)
			local altered = false
			for i,j in pairs(members) do
				if j == oldPhone then
					members[i] = newPhone
					altered = true
				end
			end
			if altered then
				sql:execute("UPDATE smartphone_whatsapp_groups SET members = \'"..json.encode(members).."\' WHERE id = \'"..v.id.."\'")
			end
		end
		
		TriggerClientEvent('smartphone:pusher', -1, 'REFRESH')
		TriggerClientEvent("Notify",source,"verde","Número atualizado.",5000)
    end,

